// owning agent: design-lead
// TODO: v0.75 rebuild — 24x36 art-pixel sprite for Reed Bramblestep. Replaces the
//       earlier 16x24 sprite shipped in Phase 1 / Phase 2 / v0.50.2. Two reasons:
//       (1) the smaller cells could not express the 12 anim keys (idle / idle_armed
//           / walk / walk_armed / jump / jump_armed / sprint / sprint_armed / attack
//           / hurt / stumble / death) without smearing — user reported "mushed"
//           pose reads.
//       (2) per-anim fps from v0.50.2 ran too hot (sprint 12 fps × 4 frames =
//           3 strides/sec at sprint speed — read as "dancing wildly"). All
//           animFps values are slowed below to match real stride frequency.
//
//       At sprite.scale = 3 (renderer convention) the new 24x36 cells render
//       as 72 × 108 logical pixels — about 1.5 tiles wide and 2.25 tiles tall.
//       The 30 × 66 player hitbox (owned by dev-lead, unchanged) sits centered
//       inside this footprint; visual overflow is intentional headroom for
//       hair lift, arm swing, and the death rise.
//
// Frame ASCII legend (per palette index):
//   .  = 0  transparent
//   o  = 1  deep moss outline (#1a2618)        — silhouette ink, never pure black
//   v  = 2  violet undershadow (#3a2e4a)       — under-jaw / under-tunic shade
//   s  = 3  dawn-amber skin (#e8a878)          — face / arms / feet
//   S  = 4  skin shadow (#a8704a)              — cheek / knuckle / heel
//   h  = 5  hair ginger-warm (#c25a30)
//   H  = 6  hair shadow (#7a2e18)
//   t  = 7  tunic moss-green base (#4a7c3a)
//   T  = 8  tunic shadow (#2e5028)
//   c  = 9  cuff-cream highlight (#e8d4a0)     — collar / cuff / hem
//   p  = a  pouch river-stone-grey (#6e7a82)
//   P  = b  pouch dark (#3e4850)
//   r  = c  river-stone pebble (#d8c8a8)        — visible loaded stone & belt buckle
//   k  = d  chip-stone-grey (#7e858e)           — hatchet head main face
//   K  = e  chip-stone-grey shadow (#5a6068)    — hatchet head dark face
//   w  = f  cloth-wrap-tan (#c89a68)            — hatchet handle binding
//   a  = 16 amber-underglow wisp (#f8b860)      — sprint trail (rhymes w/ Hummerwing amber)
//   u  = 17 pale-violet wisp (#a888b0)          — sprint trail (cool sibling of velvet ink)
//   F  = 18 skin highlight (#f4c898)            — v0.75 NEW: forehead/cheekbone catchlight,
//                                                  nose/knuckle pop at the higher 24x36 res.
//                                                  Lifted from same dawn-amber family as
//                                                  index 3 (skin). One pale step lighter.
//   M  = 19 moss-mid highlight (#5a8a4a)        — v0.75 reuse of an existing project hex
//                                                  (cairn-mantle moss / pale moss highlight,
//                                                  Phase 3 boss + dark-forest). Used here
//                                                  for the tunic shoulder catchlight and
//                                                  the top edge of the cuff fold so the
//                                                  torso reads as lit-from-above without
//                                                  introducing a fresh hex.
//
// Authoring note — frame coherence
// The "dancing wildly" complaint was partly fps and partly per-frame body delta.
// In this rebuild adjacent frames share a stable head + torso silhouette; only
// the limbs (legs, free arm, hatchet) move between cells. The walk cycle in
// particular keeps the head exactly where it was in the previous frame — only
// the legs swing — so even at brisk playback the pose does not strobe.

